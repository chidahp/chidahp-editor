// --- Core Engine: Text Parsing & Pagination ---
function processText() {
  state.rawText = editorInput.value;
  
  // 1. Break text into logical blocks
  // A double newline or more is a paragraph break (เคาะย่อหน้า = เคาะ 2 บรรทัด)
  const blocks = state.rawText.split(/\n\n+/);
  
  const parsedItems = [];
  let activeChapter = "ตอนที่ 1";
  
  blocks.forEach((block, index) => {
    const trimmed = block.trim();
    if (!trimmed) return;
    
    // Check if it's a chapter title
    if (trimmed.startsWith("[CHP]") || trimmed.startsWith("ตอนที่") || trimmed.startsWith("บทที่")) {
      const title = trimmed.replace("[CHP]", "").trim();
      activeChapter = title;
      parsedItems.push({
        type: "chapter",
        content: title,
        rawIndex: index
      });
      return;
    }
    
    // Check if it's a manual page break
    if (trimmed === "[PAGE]") {
      parsedItems.push({
        type: "pagebreak",
        rawIndex: index
      });
      return;
    }
    
    // Check if it's a book divider
    if (trimmed === "[DIVIDER]" || trimmed === "✥") {
      parsedItems.push({
        type: "divider",
        rawIndex: index
      });
      return;
    }
    
    // Check if it's a pause indicator
    if (trimmed === "..." || trimmed === "…") {
      parsedItems.push({
        type: "pause",
        rawIndex: index
      });
      return;
    }
    
    // Check if it's a Hook line
    // Wrapped in [H] or starts/ends with [H]
    const hookRegex = /^\[H\]([\s\S]+?)\[H\]$/;
    if (hookRegex.test(trimmed)) {
      const content = trimmed.match(hookRegex)[1].trim();
      parsedItems.push({
        type: "hook",
        content: content,
        rawIndex: index
      });
      return;
    }
    
    // Fallback: normal paragraph/ideas
    // Normal paragraphs can have single newlines representing "เคาะใจความ = เคาะ 1 บรรทัด"
    const lines = trimmed.split("\n");
    parsedItems.push({
      type: "paragraph",
      lines: lines.map(l => l.trim()),
      rawIndex: index
    });
  });
  
  // 2. Paginate based on the line budget & Cheedab Rules
  const pages = [];
  let currentPage = {
    chapter: activeChapter,
    items: [],
    lineCount: 0,
    isChapterStart: false
  };
  
  const startNewPage = (chapterName = activeChapter, isChapterStart = false) => {
    if (currentPage.items.length > 0) {
      pages.push(currentPage);
    }
    currentPage = {
      chapter: chapterName,
      items: [],
      lineCount: 0,
      isChapterStart: isChapterStart
    };
  };
  
  parsedItems.forEach((item) => {
    if (item.type === "chapter") {
      // Rule 1: A new chapter starts at a new page, taking a "half-page" visual opening
      startNewPage(item.content, true);
      currentPage.items.push(item);
      currentPage.lineCount += 7; // Estimate chapter heading & spacer as 7 lines
      activeChapter = item.content;
      return;
    }
    
    if (item.type === "pagebreak") {
      startNewPage(activeChapter);
      return;
    }
    
    // Calculate cost (lines) of current item
    let cost = 0;
    if (item.type === "paragraph") {
      // 1 line per line inside paragraph + 1 empty line for the paragraph space below it
      cost = item.lines.length + 1;
    } else if (item.type === "hook") {
      // 1 line for text + 2 blank lines (above & below)
      cost = 3;
    } else if (item.type === "pause") {
      cost = 2; // text + spacing
    } else if (item.type === "divider") {
      cost = 3; // divider logo + spacing
    }
    
    // Rule: Don't exceed maximum lines per page.
    // If it exceeds, push to new page.
    if (currentPage.lineCount + cost > state.maxLinesPerPage && currentPage.items.length > 0) {
      
      // Proactive orphan prevention:
      // If we are pushing a paragraph and it's 3 lines long, and it's split, rather than splitting it, we push the whole paragraph.
      // If it's a hook, we absolutely push it to the next page.
      startNewPage(activeChapter);
    }
    
    // If we are starting a new page and the first item is a Hook,
    // "กฎ 2 บรรทัดคม: ผลักประโยคคมขึ้นหน้าใหม่ ให้เคาะ 1 บรรทัดก่อนเสมอเพื่อให้บาลานซ์"
    if (currentPage.lineCount === 0 && item.type === "hook") {
      item.needsTopSpacing = true;
    } else {
      item.needsTopSpacing = false;
    }
    
    currentPage.items.push(item);
    currentPage.lineCount += cost;
  });
  
  // Push the final page
  if (currentPage.items.length > 0 || pages.length === 0) {
    pages.push(currentPage);
  }
  
  state.parsedPages = pages;
  
  // 3. Make sure currentPageIndex is within valid bounds
  const totalSpreads = Math.ceil(state.parsedPages.length / 2);
  if (state.currentPageIndex >= totalSpreads) {
    state.currentPageIndex = Math.max(0, totalSpreads - 1);
  }
  
  // 4. Update the mockup pages display
  renderBookPages();
  
  // 5. Run Linter Analysis
  runLinter();
}

// --- Render Book Pages ---
function renderBookPages() {
  const leftIdx = state.currentPageIndex * 2;
  const rightIdx = leftIdx + 1;
  
  const leftPageData = state.parsedPages[leftIdx];
  const rightPageData = state.parsedPages[rightIdx];
  
  // Set Font Family & Margins
  leftPageBody.className = `book-body ${state.fontFamily}`;
  rightPageBody.className = `book-body ${state.fontFamily}`;
  
  const applyMargins = (el) => {
    if (state.marginStyle === "narrow") {
      el.style.padding = "24px 30px";
    } else if (state.marginStyle === "wide") {
      el.style.padding = "48px 50px";
    } else {
      el.style.padding = "36px 40px"; // Medium
    }
  };
  applyMargins(document.getElementById("left-page"));
  applyMargins(document.getElementById("right-page"));
  
  // Set body font sizes
  leftPageBody.style.fontSize = `${state.fontSize}px`;
  rightPageBody.style.fontSize = `${state.fontSize}px`;
  leftPageBody.style.lineHeight = state.lineHeight;
  rightPageBody.style.lineHeight = state.lineHeight;
  
  // Render Left Page
  if (leftPageData) {
    document.getElementById("left-page").style.visibility = "visible";
    leftPageNum.textContent = leftIdx + 1;
    leftHeaderChapter.textContent = leftPageData.chapter;
    leftPageBody.innerHTML = renderPageHTML(leftPageData);
  } else {
    // Hide left page if completely empty
    document.getElementById("left-page").style.visibility = "hidden";
  }
  
  // Render Right Page
  if (rightPageData) {
    document.getElementById("right-page").style.visibility = "visible";
    rightPageNum.textContent = rightIdx + 1;
    rightHeaderChapter.textContent = rightPageData.chapter;
    rightPageBody.innerHTML = renderPageHTML(rightPageData);
  } else {
    // Make right page a blank book page
    document.getElementById("right-page").style.visibility = "visible";
    rightPageNum.textContent = rightIdx + 1;
    rightHeaderChapter.textContent = leftPageData ? leftPageData.chapter : "";
    rightPageBody.innerHTML = `
      <div style="display:flex; justify-content:center; align-items:center; height:100%; color:rgba(0,0,0,0.15); font-style:italic; font-size:0.9rem;">
        ( หน้าว่าง )
      </div>
    `;
  }
  
  // Update page flip buttons
  const totalSpreads = Math.ceil(state.parsedPages.length / 2);
  btnPrevPage.disabled = state.currentPageIndex === 0;
  btnNextPage.disabled = state.currentPageIndex >= totalSpreads - 1;
  
  pageCounter.textContent = `หน้า ${leftIdx + 1} - ${rightIdx + 1} จากทั้งหมด ${state.parsedPages.length} หน้า`;
}

// Convert Page Data into styled HTML
function renderPageHTML(pageData) {
  let html = "";
  
  // If it's a chapter starting page, simulate "ครึ่งหน้าเปิดบท" (Rule 1)
  if (pageData.isChapterStart) {
    const chItem = pageData.items.find(i => i.type === "chapter");
    html += `
      <div class="chapter-title-page">
        <div class="chapter-number">บทเปิด</div>
        <div class="chapter-name">${chItem ? chItem.content : pageData.chapter}</div>
      </div>
      <div class="half-page-start"></div>
    `;
  }
  
  pageData.items.forEach(item => {
    if (item.type === "chapter") return; // Rendered above
    
    if (item.type === "paragraph") {
      // Join ideas inside paragraph with single newlines
      const innerHTML = item.lines.map(line => {
        // Detect special highlighting or emotional punctuation
        let processedLine = line;
        
        // Match emotional highlights or double punctuation
        processedLine = processedLine.replace(/([ก-๙]+[…!？?]{2,})/g, `<strong style="color:#000;">$1</strong>`);
        
        return `<span class="cheedab-line">${processedLine}</span>`;
      }).join("");
      
      html += `<div class="cheedab-p" data-raw-index="${item.rawIndex}">${innerHTML}</div>`;
    }
    
    else if (item.type === "hook") {
      const topSpace = item.needsTopSpacing ? '<div style="height:1.2em;"></div>' : '';
      html += `${topSpace}<span class="cheedab-hook" data-raw-index="${item.rawIndex}">${item.content}</span>`;
    }
    
    else if (item.type === "pause") {
      html += `<span class="cheedab-pause" data-raw-index="${item.rawIndex}">…</span>`;
    }
    
    else if (item.type === "divider") {
      html += `
        <div class="cheedab-divider" data-raw-index="${item.rawIndex}">
          <div class="cheedab-divider-logo">
            <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><path d="M12 2l3 6 7 1-5 5 1 7-6-3-6 3 1-7-5-5 7-1z"></path></svg>
          </div>
        </div>
      `;
    }
  });
  
  return html;
}

// --- Smart Linter & Advisor Engine ---
function runLinter() {
  const warnings = [];
  const lines = state.rawText.split("\n");
  
  // 1. Check Paragraph Length (>3 lines)
  // Cheedab Rule 2: "พารากราฟของชี้ดาบ จะไม่ยาวเกิน 3 ใจความ หรือไม่เกิน 3 บรรทัด"
  const blocks = state.rawText.split(/\n\n+/);
  blocks.forEach((block, blockIdx) => {
    const trimmed = block.trim();
    if (!trimmed) return;
    
    // Ignore special triggers
    if (trimmed.startsWith("[CHP]") || trimmed.startsWith("ตอนที่") || trimmed.startsWith("บทที่") || trimmed === "[PAGE]" || trimmed === "[DIVIDER]" || trimmed === "✥" || trimmed === "..." || trimmed === "…") {
      return;
    }
    
    // If it's a hook
    if (trimmed.startsWith("[H]") && trimmed.endsWith("[H]")) {
      return;
    }
    
    const paragraphLines = trimmed.split("\n").filter(l => l.trim().length > 0);
    if (paragraphLines.length > 3) {
      warnings.push({
        type: "danger",
        title: "ย่อหน้ายาวเกินเกณฑ์ (Rule 2)",
        message: `ย่อหน้านี้มีความยาวถึง ${paragraphLines.length} บรรทัด ซึ่งเกินกฎ 3 ใจความแบบชี้ดาบ ผู้อ่านจะกวาดสายตาลำบาก`,
        actionText: "แยกเป็นย่อหน้าย่อย",
        blockText: block,
        applyFix: () => {
          // Break every 2 lines
          const chunks = [];
          for (let i = 0; i < paragraphLines.length; i += 2) {
            chunks.push(paragraphLines.slice(i, i + 2).join("\n"));
          }
          const fixedBlock = chunks.join("\n\n");
          editorInput.value = editorInput.value.replace(block, fixedBlock);
          processText();
          showToast("แยกย่อหน้าให้กระชับขึ้นเรียบร้อย!");
        }
      });
    }
  });
  
  // 2. Check Static Scenes / ซีนแช่ (Rule 3)
  // Look for keywords indicating boring time loops or repeating descriptions
  const staticSceneKeywords = ["มกรา", "กุมภา", "มีนา", "เมษา", "ช่างเป็นเดือน", "เรือนลาง", "เลื่อนลอย"];
  blocks.forEach((block) => {
    const trimmed = block.trim();
    let containsStaticKeywords = false;
    let matchWord = "";
    
    for (const kw of staticSceneKeywords) {
      if (trimmed.includes(kw)) {
        containsStaticKeywords = true;
        matchWord = kw;
        break;
      }
    }
    
    if (containsStaticKeywords && (trimmed.includes("เดือน") || trimmed.split("\n").length >= 3)) {
      warnings.push({
        type: "warning",
        title: "ตรวจพบ 'ซีนแช่' (Rule 3)",
        message: `ประโยคเกี่ยวกับ "${matchWord}..." อาจเป็นซีนแช่ที่ไม่สร้างความเปลี่ยนแปลงทางอารมณ์ใน 2-3 บรรทัด แนะนำให้พิจารณาตัดออก`,
        actionText: "ลบซีนนี้ออก",
        applyFix: () => {
          editorInput.value = editorInput.value.replace(block + "\n\n", "").replace(block, "");
          processText();
          showToast("ลบซีนแช่เพื่อความลื่นไหลเรียบร้อย!");
        }
      });
    }
  });
  
  // 3. Consecutive Single-line paragraphs (>3 in a row)
  // "และต้องไม่สั้นติดกันหลายบรรทัดเกินไป โดยคำนึงถึงความสวยงามเวลากวาดสายตา"
  let consecutiveShorts = 0;
  let shortBlocks = [];
  
  blocks.forEach((block, blockIdx) => {
    const trimmed = block.trim();
    if (!trimmed) return;
    
    if (trimmed.startsWith("[CHP]") || trimmed === "[PAGE]" || trimmed === "[DIVIDER]" || trimmed === "✥" || trimmed === "..." || trimmed === "…") {
      consecutiveShorts = 0;
      shortBlocks = [];
      return;
    }
    
    const blockLines = trimmed.split("\n").filter(l => l.trim().length > 0);
    // If it's a single line and very short (under 60 chars)
    if (blockLines.length === 1 && trimmed.length < 65) {
      consecutiveShorts++;
      shortBlocks.push(block);
    } else {
      if (consecutiveShorts >= 3) {
        // Warn about previous bunch
        warnings.push({
          type: "warning",
          title: "ย่อหน้าสั้นติดกันมากเกินไป",
          message: `มีประโยคสั้นๆ เคาะย่อหน้าแยกกันติดต่อกันถึง ${consecutiveShorts} ครั้ง ทำให้ผู้อ่านรู้สึกเหมือนเรื่องสะดุดล้า`,
          actionText: "รวมประโยคเข้าด้วยกัน",
          applyFix: () => {
            const merged = shortBlocks.join("\n");
            // Replace the sequence of short blocks in raw text
            let currentText = editorInput.value;
            // Let's create a regex to safely replace them
            const searchStr = shortBlocks.join("\n\n");
            if (currentText.includes(searchStr)) {
              currentText = currentText.replace(searchStr, merged);
            } else {
              // Fallback replace individually
              shortBlocks.forEach((sb, idx) => {
                if (idx > 0) {
                  currentText = currentText.replace("\n\n" + sb, "\n" + sb);
                }
              });
            }
            editorInput.value = currentText;
            processText();
            showToast("รวมกลุ่มคำย่อหน้าเพื่อความสวยงามเรียบร้อย!");
          }
        });
      }
      consecutiveShorts = 0;
      shortBlocks = [];
    }
  });
  
  // Make sure we catch trailing ones
  if (consecutiveShorts >= 3) {
    warnings.push({
      type: "warning",
      title: "ย่อหน้าสั้นติดกันมากเกินไป",
      message: `ประโยคตอนท้ายมีสัดส่วนสั้นติดกันหลายย่อหน้าเกินไป (${consecutiveShorts} ย่อหน้า)`,
      actionText: "รวมประโยคเพื่อความสมูท",
      applyFix: () => {
        const merged = shortBlocks.join("\n");
        let currentText = editorInput.value;
        const searchStr = shortBlocks.join("\n\n");
        if (currentText.includes(searchStr)) {
          currentText = currentText.replace(searchStr, merged);
        }
        editorInput.value = currentText;
        processText();
        showToast("ปรับสมดุลสายตาเรียบร้อย!");
      }
    });
  }
  
  // 4. Chapter Opening check (Rule 1)
  // "จำนวนบรรทัดในการเปิดครึ่งหน้า จะอยู่ที่ประมาณ 7-9 บรรทัด และจบด้วยประโยคที่ชวนอ่านต่อ"
  state.parsedPages.forEach((page, pageIdx) => {
    if (page.isChapterStart) {
      // Find the count of lines on this page excluding chapter title
      let paragraphLineCount = 0;
      page.items.forEach(item => {
        if (item.type === "paragraph") {
          paragraphLineCount += item.lines.length;
        }
      });
      
      if (paragraphLineCount > 10 || paragraphLineCount < 5) {
        warnings.push({
          type: "warning",
          title: `หน้าแรกของบทที่ ${pageIdx + 1} ไม่สมดุล (Rule 1)`,
          message: `พบว่าหน้าเปิดบทใหม่มีคำบรรยายอยู่ ${paragraphLineCount} บรรทัด ซึ่งเกณฑ์ที่ดีที่สุดควรอยู่ที่ 7-9 บรรทัดเพื่อให้ดูสบายตาในครึ่งหน้ากระดาษพอดี`,
          actionText: "ตรวจสอบหน้านี้",
          applyFix: () => {
            // Navigate to this page
            state.currentPageIndex = Math.floor(pageIdx / 2);
            renderBookPages();
            showToast(`เปิดไปยังหน้า ${pageIdx + 1} เพื่อให้คุณปรับแต่งจำนวนคำดั่งใจ`);
          }
        });
      }
    }
  });

  // Render warnings in sidebar
  renderLinterUI(warnings);
}

// Render Linter reports in the Left Sidebar
function renderLinterUI(warnings) {
  if (warnings.length === 0) {
    linterContainer.innerHTML = `
      <div class="empty-linter">
        <svg width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><polyline points="20 6 9 17 4 12"></polyline></svg>
        <span>จังหวะการเล่าเรื่องลื่นไหลยอดเยี่ยม! ไม่พบข้อผิดพลาดตามกฎชี้ดาบ</span>
      </div>
    `;
    return;
  }
  
  linterContainer.innerHTML = "";
  warnings.forEach((warn) => {
    const div = document.createElement("div");
    div.className = `linter-item ${warn.type}`;
    div.innerHTML = `
      <div class="linter-header">
        <span>${warn.title}</span>
        <span class="badge">${warn.type === "danger" ? "ควรปรับปรุง" : "แนะนำ"}</span>
      </div>
      <div class="linter-desc">${warn.message}</div>
      <div class="linter-action">${warn.actionText} →</div>
    `;
    
    div.addEventListener("click", () => {
      warn.applyFix();
    });
    
    linterContainer.appendChild(div);
  });
}

// --- Editor Utilities & Toolbar Features ---
function syncLineNumbers() {
  const text = editorInput.value;
  const lines = text.split("\n");
  const count = lines.length;
  let html = "";
  for (let i = 1; i <= count; i++) {
    html += `${i}<br>`;
  }
  lineNumbersCol.innerHTML = html;
}

// Apply Selected Formatting
function insertAtCursor(before, after) {
  const start = editorInput.selectionStart;
  const end = editorInput.selectionEnd;
  const text = editorInput.value;
  const selectedText = text.substring(start, end);
  
  const replacement = before + selectedText + after;
  editorInput.value = text.substring(0, start) + replacement + text.substring(end);
  
  // Set focus back
  editorInput.focus();
  editorInput.selectionStart = start + before.length;
  editorInput.selectionEnd = start + before.length + selectedText.length;
  
  processText();
  syncLineNumbers();
}

// Toggle Hook
function toggleHookSelection() {
  const start = editorInput.selectionStart;
  const end = editorInput.selectionEnd;
  const text = editorInput.value;
  
  // Find current line bounds
  let lineStart = text.lastIndexOf("\n", start - 1) + 1;
  let lineEnd = text.indexOf("\n", end);
  if (lineEnd === -1) lineEnd = text.length;
  
  const selectedLine = text.substring(lineStart, lineEnd);
  
  // Check if it already has hook format
  if (selectedLine.startsWith("[H]") && selectedLine.endsWith("[H]")) {
    // Remove
    const cleaned = selectedLine.substring(3, selectedLine.length - 3).trim();
    editorInput.value = text.substring(0, lineStart) + cleaned + text.substring(lineEnd);
    showToast("เอาลักษณะ Hook ออกแล้ว");
  } else {
    // Add
    const wrapped = `\n[H] ${selectedLine.trim()} [H]\n`;
    editorInput.value = text.substring(0, lineStart) + wrapped + text.substring(lineEnd);
    showToast("ตั้งค่าเป็นประโยค Hook สำเร็จ!");
  }
  
  // Clean double empty lines created by wrapping
  editorInput.value = editorInput.value.replace(/\n{3,}/g, "\n\n");
  
  processText();
  syncLineNumbers();
}

// Indent Cleanup (จัดย่อหน้าเรียบร้อย)
function cleanIndentation() {
  const text = editorInput.value;
  const paragraphs = text.split(/\n\n+/);
  
  const cleaned = paragraphs.map(p => {
    const trimmed = p.trim();
    if (trimmed.startsWith("[CHP]") || trimmed === "[PAGE]" || trimmed === "[DIVIDER]" || trimmed === "✥" || trimmed === "..." || trimmed === "…") {
      return trimmed;
    }
    if (trimmed.startsWith("[H]") && trimmed.endsWith("[H]")) {
      return trimmed;
    }
    // Clean spaces
    return trimmed.split("\n").map(line => line.trim()).join("\n");
  }).join("\n\n");
  
  editorInput.value = cleaned;
  processText();
  syncLineNumbers();
  showToast("จัดกลุ่มอักษรเรียบร้อยสวยงาม!");
}

// Auto-Format text based on Cheedab formatting rules automatically
function autoFormatText(text) {
  if (!text.trim()) return "";
  
  let lines = text.split("\n").map(l => l.trim());
  let output = [];
  
  let consecutiveShortLinesCount = 0;
  let inChapterOpening = false;
  let chapterLineCount = 0;
  
  for (let i = 0; i < lines.length; i++) {
    let line = lines[i];
    
    // If it's a completely empty line, just push empty line
    if (!line) {
      output.push("");
      continue;
    }
    
    // 1. Detect Chapter Titles
    if (line.startsWith("[CHP]") || line.startsWith("ตอนที่") || line.startsWith("บทที่") || line.startsWith("บทนำ")) {
      let cleanTitle = line.replace("[CHP]", "").trim();
      output.push(`[CHP] ${cleanTitle}`);
      inChapterOpening = true;
      chapterLineCount = 0;
      continue;
    }
    
    // If it's a special marker, keep it
    if (line === "[PAGE]" || line === "[DIVIDER]" || line === "✥" || line === "..." || line === "…") {
      output.push(line);
      if (line === "[PAGE]") {
        inChapterOpening = false;
      }
      continue;
    }
    
    // Check if it's already a Hook
    if (line.startsWith("[H]") && line.endsWith("[H]")) {
      output.push(line);
      if (inChapterOpening) {
        chapterLineCount++;
        if (chapterLineCount === 8) {
          output.push("[PAGE]");
          inChapterOpening = false;
        }
      }
      continue;
    }
    
    // 2. Track chapter opening and inject PAGE break after 8 lines
    if (inChapterOpening) {
      chapterLineCount++;
      if (chapterLineCount === 8) {
        output.push(line);
        output.push("[PAGE]"); // Proactively break to keep 7-9 lines in the opening half-page
        inChapterOpening = false;
        continue;
      }
    }
    
    // 3. Auto-detect Hook:
    // If it is in quotation marks and is very short (<50 chars) and isolated or stands as dialog
    const isQuoted = (line.startsWith("“") && line.endsWith("”")) || (line.startsWith('"') && line.endsWith('"')) || (line.startsWith("'") && line.endsWith("'"));
    if (isQuoted && line.length < 50) {
      output.push(`[H] ${line} [H]`);
      continue;
    }
    
    // 4. Split long Thai sentences (ideas) without newlines
    // If a single line is extremely long (>130 chars), split it at spaces to make it neat rhythmic lines (เคาะใจความ)
    if (line.length > 130 && line.includes(" ")) {
      let parts = line.split(" ");
      let currentPart = "";
      parts.forEach(part => {
        if ((currentPart + " " + part).length > 80) {
          output.push(currentPart.trim());
          currentPart = part;
          if (inChapterOpening) {
            chapterLineCount++;
          }
        } else {
          currentPart += (currentPart ? " " : "") + part;
        }
      });
      if (currentPart) {
        output.push(currentPart.trim());
        if (inChapterOpening) {
          chapterLineCount++;
        }
      }
      if (inChapterOpening && chapterLineCount >= 8) {
        output.push("[PAGE]");
        inChapterOpening = false;
      }
      continue;
    }
    
    output.push(line);
  }
  
  // Rebuild the text and do paragraph-level formatting
  let rebuiltText = output.join("\n");
  let blocks = rebuiltText.split(/\n\n+/);
  let finalBlocks = [];
  
  let consecutiveShortParagraphs = [];
  
  for (let b = 0; b < blocks.length; b++) {
    let block = blocks[b].trim();
    if (!block) continue;
    
    // Keep special blocks unchanged
    if (block.startsWith("[CHP]") || block === "[PAGE]" || block === "[DIVIDER]" || block === "✥" || block === "..." || block === "…") {
      finalBlocks.push(block);
      continue;
    }
    
    // If it is already a hook, keep it
    if (block.startsWith("[H]") && block.endsWith("[H]")) {
      finalBlocks.push(block);
      continue;
    }
    
    // Static Scene Cleaner:
    // If it's a known static scene, comment it out or replace with a nice transition
    if (block.includes("มกรา") && block.includes("กุมภา") && (block.includes("เลื่อนลอย") || block.includes("เรือนลาง"))) {
      finalBlocks.push("... หลายเดือนผ่านไปอย่างเลื่อนลอย");
      continue;
    }
    
    let blockLines = block.split("\n").map(l => l.trim()).filter(l => l.length > 0);
    
    // 5. Paragraph splits (Rule 2):
    // If a paragraph has more than 3 lines, split it into smaller ones (2 lines each)
    if (blockLines.length > 3) {
      let chunks = [];
      for (let i = 0; i < blockLines.length; i += 2) {
        chunks.push(blockLines.slice(i, i + 2).join("\n"));
      }
      chunks.forEach(chunk => finalBlocks.push(chunk));
      continue;
    }
    
    // 6. Balance consecutive single short lines:
    // If we have single short lines, group them in pairs of 2 lines to make it visually beautiful
    if (blockLines.length === 1 && block.length < 60) {
      consecutiveShortParagraphs.push(block);
    } else {
      if (consecutiveShortParagraphs.length > 0) {
        for (let i = 0; i < consecutiveShortParagraphs.length; i += 2) {
          if (i + 1 < consecutiveShortParagraphs.length) {
            finalBlocks.push(consecutiveShortParagraphs[i] + "\n" + consecutiveShortParagraphs[i+1]);
          } else {
            finalBlocks.push(consecutiveShortParagraphs[i]);
          }
        }
        consecutiveShortParagraphs = [];
      }
      finalBlocks.push(block);
    }
  }
  
  // Drain remaining short paragraphs
  if (consecutiveShortParagraphs.length > 0) {
    for (let i = 0; i < consecutiveShortParagraphs.length; i += 2) {
      if (i + 1 < consecutiveShortParagraphs.length) {
        finalBlocks.push(consecutiveShortParagraphs[i] + "\n" + consecutiveShortParagraphs[i+1]);
      } else {
        finalBlocks.push(consecutiveShortParagraphs[i]);
      }
    }
  }
  
  return finalBlocks.join("\n\n");
}

// --- Event Listeners ---
function attachEventListeners() {
  // Real-time Editor input sync
  editorInput.addEventListener("input", () => {
    processText();
    syncLineNumbers();
  });
  
  editorInput.addEventListener("scroll", () => {
    lineNumbersCol.scrollTop = editorInput.scrollTop;
  });
  
  // Sidebar config changes
  configFontFamily.addEventListener("change", (e) => {
    state.fontFamily = e.target.value;
    processText();
  });
  
  configFontSize.addEventListener("input", (e) => {
    state.fontSize = parseInt(e.target.value);
    valFontSize.textContent = `${state.fontSize}px`;
    processText();
  });
  
  configLinesPage.addEventListener("input", (e) => {
    state.maxLinesPerPage = parseInt(e.target.value);
    valLinesPage.textContent = `${state.maxLinesPerPage} บรรทัด`;
    processText();
  });
  
  configLineHeight.addEventListener("input", (e) => {
    state.lineHeight = parseFloat(e.target.value);
    valLineHeight.textContent = state.lineHeight.toFixed(1);
    processText();
  });
  
  configMargins.addEventListener("change", (e) => {
    state.marginStyle = e.target.value;
    valMargins.textContent = e.target.options[e.target.selectedIndex].text.split(" ")[0];
    processText();
  });
  
  // Page navigation
  btnPrevPage.addEventListener("click", () => {
    if (state.currentPageIndex > 0) {
      state.currentPageIndex--;
      renderBookPages();
    }
  });
  
  btnNextPage.addEventListener("click", () => {
    const totalSpreads = Math.ceil(state.parsedPages.length / 2);
    if (state.currentPageIndex < totalSpreads - 1) {
      state.currentPageIndex++;
      renderBookPages();
    }
  });
  
  // Toolbar buttons actions
  toolHook.addEventListener("click", toggleHookSelection);
  
  toolPause.addEventListener("click", () => {
    insertAtCursor("\n...\n", "");
    showToast("แทรกจุดพักสายตาสำเร็จ");
  });
  
  toolDivider.addEventListener("click", () => {
    insertAtCursor("\n[DIVIDER]\n", "");
    showToast("แทรกตัวคั่นเว้นความล้าเรียบร้อย");
  });
  
  toolIndent.addEventListener("click", cleanIndentation);
  
  toolPush.addEventListener("click", () => {
    insertAtCursor("\n[PAGE]\n", "");
    showToast("ผลักขึ้นหน้าใหม่สำเร็จ!");
  });
  
  // Clear button
  btnClear.addEventListener("click", () => {
    if (confirm("คุณต้องการล้างข้อความทั้งหมดในห้องทำงานใช่หรือไม่?")) {
      editorInput.value = "";
      processText();
      syncLineNumbers();
      showToast("ล้างพื้นที่ทำงานเรียบร้อย");
    }
  });
  
  // Auto format button action
  btnAutoFormat.addEventListener("click", () => {
    const raw = editorInput.value;
    if (!raw.trim()) {
      showToast("กรุณาป้อนข้อความก่อนจัดจังหวะอัตโนมัติ!");
      return;
    }
    const formatted = autoFormatText(raw);
    editorInput.value = formatted;
    state.currentPageIndex = 0;
    processText();
    syncLineNumbers();
    showToast("จัดหน้าและปรับจังหวะชี้ดาบอัตโนมัติเรียบร้อย! ✨");
  });

  // Preset & Tutorial Modal triggers
  btnExamples.addEventListener("click", () => {
    tutorialOverlay.classList.add("active");
  });
  
  const closeModal = () => {
    tutorialOverlay.classList.remove("active");
  };
  btnCloseModal.addEventListener("click", closeModal);
  btnCloseModalFooter.addEventListener("click", closeModal);
  
  // Load Presets when clicking cards inside modal
  const cards = document.querySelectorAll(".preset-card");
  cards.forEach(card => {
    card.addEventListener("click", () => {
      const presetName = card.getAttribute("data-preset");
      if (PRESETS[presetName]) {
        editorInput.value = PRESETS[presetName];
        state.currentPageIndex = 0;
        processText();
        syncLineNumbers();
        closeModal();
        showToast(`โหลดตัวอย่าง "${card.querySelector(".preset-title").textContent}" เรียบร้อย!`);
      }
    });
  });
  
  // Export button
  btnExport.addEventListener("click", () => {
    // Generate clean formatted output
    // Replace markdown helper symbols with elegant layout strings or let user download text
    const cleanOutput = editorInput.value
      .replace(/\[H\]/g, "")
      .replace(/\[DIVIDER\]/g, "✥")
      .replace(/\[PAGE\]/g, "---")
      .replace(/\[CHP\]/g, "บทเปิด:");
      
    // Simple file download trigger
    const blob = new Blob([cleanOutput], { type: "text/plain;charset=utf-8" });
    const url = URL.createObjectURL(blob);
    const link = document.createElement("a");
    link.href = url;
    link.download = "cheedab_formatted_novel.txt";
    document.body.appendChild(link);
    link.click();
    document.body.removeChild(link);
    
    showToast("ส่งออกบทประพันธ์เสร็จสิ้น! บันทึกไฟล์เรียบร้อย");
  });
}

